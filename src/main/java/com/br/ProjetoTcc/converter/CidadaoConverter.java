package com.br.ProjetoTcc.converter;

import com.br.ProjetoTcc.cidadao.Cidadao;
import jakarta.faces.component.UIComponent;
import jakarta.faces.context.FacesContext;
import jakarta.faces.convert.Converter;
import jakarta.faces.convert.FacesConverter;

/**
 *
 * @author johnny
 */

@FacesConverter(value = "CidadaoConverter", forClass = Cidadao.class)
public class CidadaoConverter implements Converter {
    
    @Override
    public Object getAsObject(FacesContext facesContext, UIComponent uiComponent, String value) {
        if (value != null && !value.isEmpty()) {
            return (Cidadao) uiComponent.getAttributes().get(value);
        }

        return null;
    }

    @Override
    public String getAsString(FacesContext facesContext, UIComponent uiComponent, Object value) {

        if (value instanceof Cidadao) {

            Cidadao entity = (Cidadao) value;

            if (entity != null && entity instanceof Cidadao && entity.getId() != null) {
                uiComponent.getAttributes().put(entity.getId().toString(), entity);
                return entity.getId().toString();
            }
        }
        return "";
    }
    
}
